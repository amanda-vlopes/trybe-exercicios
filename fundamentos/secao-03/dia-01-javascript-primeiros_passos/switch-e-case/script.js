let resultado = 'aprovado';
switch (resultado) {
	case 'aprovado':
		console.log('Parabéns você foi aprovada(o)!');
		break;

	case 'reprovado':
		console.log('Você foi reprovada(o)!');
		break;

	case 'lista':
		console.log('Você está na nossa lista de espera');
		break;
		
	default:
		console.log('Informação incorreta');
}

